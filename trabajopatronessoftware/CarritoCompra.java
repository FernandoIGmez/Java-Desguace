    //Rellena la tabla con las compras realizadas por el usuario
    public void rellenarTabla(){
       piezasCarrito=cliente.getCarritoCompra();
        
       while (modeloTabla.getRowCount()>0)         
        {
            modeloTabla.removeRow(0);
        } 
       
      // ArrayList <Pieza> piezasCarrito = cliente.getCarritoCompra();
       
       for (int i=0 ; i< piezasCarrito.size();i++){
       
            modeloTabla.addRow(new Object[]{piezasCarrito.get(i).getNombre(),
                            piezasCarrito.get(i).getMarca(),
                            piezasCarrito.get(i).getModelo(),
                            String.valueOf(piezasCarrito.get(i).getAño()),
                            piezasCarrito.get(i).getEstado(),
                            Float.toString(piezasCarrito.get(i).getPrecio())});
                    }
       DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
            tcr.setHorizontalAlignment(SwingConstants.CENTER);
            jTable1.getColumnModel().getColumn(0).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(1).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(2).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(3).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(4).setCellRenderer(tcr);
            jTable1.getColumnModel().getColumn(5).setCellRenderer(tcr);
    }
    //MUESTRA EN EL JFRAME EL NUMERO DE PIEZAS QUE HAN SIDO SELECCIONADAS Y CUAL ES EL PRECIO A PAAGR POR ELLAS
    public void piezasSeleccionadas(){
        int posicionPiezas [] = jTable1.getSelectedRows();
        if (posicionPiezas.length>0){
            jTextFieldNumeroPiezas.setText(posicionPiezas.length+"");
            float precioPiezas=0f;
            for (int i=0;i<posicionPiezas.length;i++){
                precioPiezas += piezasCarrito.get(posicionPiezas[i]).getPrecio();
            }
            jTextFieldPrecioPiezas.setText(""+precioPiezas);
        }
        else{
            jTextFieldNumeroPiezas.setText("0");
            jTextFieldPrecioPiezas.setText("0.0");
        }
    }
    
