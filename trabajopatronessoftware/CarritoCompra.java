    private void jButtonComprarDelCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComprarDelCarritoActionPerformed
        // TODO add your handling code here:
        ArrayList<Pieza> arrayAuxiliar = new ArrayList<Pieza>();
        int piezasCarritoAñadir[]=jTable1.getSelectedRows();
        
        //Si el usuario ha seleccionado alguna pieza del carrito para comprar 
        if (piezasCarritoAñadir.length<=0){
            JOptionPane.showMessageDialog(this, "Asegurate de que has escogido al menos un articulo para comprar","Error realizar compra",JOptionPane.WARNING_MESSAGE);  
        }
        //Si el usuario ha seleccionado alguna pieza del carrito para comprar
        else{
            for (int i=0;i<piezasCarritoAñadir.length;i++){
                //Añade al array auxiliar las piezas seleccionadas del carrito
                arrayAuxiliar.add(piezasCarrito.get(piezasCarritoAñadir[i]));
            }
            int respuesta = JOptionPane.showConfirmDialog(this, "Si quiere la compra genere un ticket pulse SI y CANCELAR para cancelar la operacion", "Compra", JOptionPane.YES_NO_CANCEL_OPTION);
            if (respuesta == 1 ){   //SI
                //Se añade la venta al usuario
                cliente.añadirVenta(factoriaVentas.getVenta(true, arrayAuxiliar));
                //Se eliminan las piezas del sistema
                for (int i=0; i<arrayAuxiliar.size();i++){
                    datos.eliminarPieza(arrayAuxiliar.get(i));
                    cliente.eliminarPiezaCarrito(arrayAuxiliar.get(i));
                }
            }
            else if(respuesta == 0){  //NO 
                //Se añade la venta al usuario
                cliente.añadirVenta(factoriaVentas.getVenta(false, arrayAuxiliar));
                //Se eliminan las piezas del sistema
                for (int i=0; i<arrayAuxiliar.size();i++){
                    datos.eliminarPieza(arrayAuxiliar.get(i));
                    cliente.eliminarPiezaCarrito(arrayAuxiliar.get(i));
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "Compra cancelada","Compra",JOptionPane.WARNING_MESSAGE);  
            }
            rellenarTabla();
        }
        
    }//GEN-LAST:event_jButtonComprarDelCarritoActionPerformed

    private void jButtonEliminarDelCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarDelCarritoActionPerformed
        // TODO add your handling code here:
        int posicionesParaEliminar[]=jTable1.getSelectedRows();
        if (posicionesParaEliminar.length<1){
            JOptionPane.showMessageDialog(this, "Por favor, seleccione uno o varios elementos para que puedan ser eliminados del carrito","Error de borrado",JOptionPane.WARNING_MESSAGE);  
        }
        else{
            for (int i=0; i < posicionesParaEliminar.length; i++){
            cliente.eliminarPiezaCarrito(piezasCarrito.get(posicionesParaEliminar[i]));
            }
            if (posicionesParaEliminar.length>1)
            JOptionPane.showMessageDialog(this, "Las piezas han sido borradas con éxito","Borrado de piezas",JOptionPane.INFORMATION_MESSAGE);  
            else
            JOptionPane.showMessageDialog(this, "La pieza ha sido borrada con éxito","Borrado de piezas",JOptionPane.INFORMATION_MESSAGE);    
            rellenarTabla();
        }
    }//GEN-LAST:event_jButtonEliminarDelCarritoActionPerformed

