    public PaginaPrincipal(DatosPrograma d , Usuario u){//String nombreusuario , String dni, boolean admin) {
        usuario= u ;
        administrador=usuario.isEsAdministrador();
        dniUsuario=usuario.getDni();
        initComponents();
        datos=d;
     //   originador= o;
        this.setTitle("Bienvenido, "+usuario.getNombre());
        
        
        if (administrador){
            jLabelNombre1.setText("Administrador");
            jButton2.setText("CONSULTA DE VENTAS");
            jButton3.setVisible(false);
            jButton4.setVisible(false);
            Dimension a=this.getSize();
            a.height = a.height-80;
            this.setSize(a);
            
        }
        else{
            jLabelNombre1.setText("Usuario");
            jButton2.setText("CARRITO DE COMPRA");
            Cliente user = (Cliente) usuario;
            
            if(user.tieneCuentaPremium()){
                jButton3.setText("DESACTIVAR SUBSCRIPCION PREMIUM");
            }
            else{
                jButton3.setText("ACTIVAR SUBSCRIPCION PREMIUM");
            }
            jButton4.setText("HISTORIAL DE COMPRAS");
         
        }

    }

